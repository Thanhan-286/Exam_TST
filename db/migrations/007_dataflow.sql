-- CHÍNH SÁCH DỮ LIỆU KHI UPLOAD:
--   Mỗi file upload là một SNAPSHOT. Batch loaded có batch_id lớn nhất là
--   snapshot active; toàn bộ stg/mart chỉ đọc snapshot này. Nhờ vậy nếu file
