-- Điểm gãy #6: dim_month lấy union tháng từ CẢ BA nguồn, không chỉ plan
create view mart.dim_month as
with m as (
  -- Excel/seed plan có thể lưu ngày cuối tháng trước (vd 2025-12-31
  -- cho plan 01/2026). Cộng 1 ngày rồi truncate để đưa về tháng kế hoạch.
  select distinct date_trunc('month', month_start + interval '1 day')::date as month_start
  from stg.raw_plan_monthly_sales
  union
  select distinct date_trunc('month', doc_date)::date from stg.raw_fact_sales_orders
  union
  select distinct date_trunc('month', month_end)::date from stg.raw_fact_inventory_eom
)
select
  month_start,
  (month_start + interval '1 month - 1 day')::date as month_end,
  to_char(month_start, 'MM/YYYY')                  as month_label,
  extract(month from month_start)::int             as month_number,
  extract(year  from month_start)::int             as year_number
from m
where month_start is not null;

