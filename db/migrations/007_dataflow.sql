-- =====================================================================
-- A. BATCH TRACKING — đường vào của dữ liệu
-- =====================================================================
create table if not exists raw.load_batches (
  batch_id    bigint generated by default as identity primary key,
  file_name   text not null,
  file_hash   text not null unique,       -- chặn upload trùng nguyên file
  row_counts  jsonb,                      -- {"fact_sales_orders": 452, ...}
  status      text not null default 'loaded'
              check (status in ('loaded','rolled_back')),
  loaded_at   timestamptz not null default now(),
  loaded_by   text
);

-- Batch #1 = seed từ Data_set.xlsx (001b_seed.sql / etl/load.py)
insert into raw.load_batches (batch_id, file_name, file_hash, status, loaded_by)
values (1, 'Data_set.xlsx (seed)', 'seed-001b', 'loaded', 'migration')
on conflict (file_hash) do nothing;
select setval(pg_get_serial_sequence('raw.load_batches','batch_id'),
              greatest((select max(batch_id) from raw.load_batches), 1));

-- batch_id trên mọi bảng raw. Đây là LINEAGE, không phải business data —
-- không vi phạm nguyên tắc "raw 0 constraint": FK về metadata không chặn
-- được bất kỳ dữ liệu bẩn nghiệp vụ nào.
alter table raw.fact_sales_orders  add column if not exists batch_id bigint not null default 1;
alter table raw.fact_inventory_eom add column if not exists batch_id bigint not null default 1;
alter table raw.dim_product        add column if not exists batch_id bigint not null default 1;
alter table raw.dim_customer       add column if not exists batch_id bigint not null default 1;
alter table raw.dim_warehouse      add column if not exists batch_id bigint not null default 1;
alter table raw.plan_monthly_sales add column if not exists batch_id bigint not null default 1;
alter table raw.data_quality_hint  add column if not exists batch_id bigint not null default 1;

do $$
declare t text;
begin
  foreach t in array array['fact_sales_orders','fact_inventory_eom','dim_product',
                           'dim_customer','dim_warehouse','plan_monthly_sales',
                           'data_quality_hint']
  loop
    if not exists (
      select 1 from pg_constraint
      where conname = t || '_batch_fk' and connamespace = 'raw'::regnamespace
    ) then
      execute format(
        'alter table raw.%I add constraint %I foreign key (batch_id)
         references raw.load_batches(batch_id)', t, t || '_batch_fk');
    end if;
  end loop;
end $$;

-- Rollback một lần upload hỏng. SECURITY DEFINER để client (anon) gọi
-- được qua RPC mà không cần quyền DELETE trực tiếp trên raw.
create or replace function raw.rollback_batch(p_batch_id bigint)
returns jsonb
language plpgsql security definer set search_path = raw
as $$
declare
  n bigint; deleted jsonb := '{}'::jsonb;
begin
  if p_batch_id = 1 then
    raise exception 'Không thể rollback batch seed #1';
  end if;
  if not exists (select 1 from raw.load_batches
                 where batch_id = p_batch_id and status = 'loaded') then
    raise exception 'Batch % không tồn tại hoặc đã rollback', p_batch_id;
  end if;

  delete from raw.fact_sales_orders  where batch_id = p_batch_id;
  get diagnostics n = row_count; deleted := deleted || jsonb_build_object('fact_sales_orders', n);
  delete from raw.fact_inventory_eom where batch_id = p_batch_id;
  get diagnostics n = row_count; deleted := deleted || jsonb_build_object('fact_inventory_eom', n);
  delete from raw.dim_product        where batch_id = p_batch_id;
  get diagnostics n = row_count; deleted := deleted || jsonb_build_object('dim_product', n);
  delete from raw.dim_customer       where batch_id = p_batch_id;
  get diagnostics n = row_count; deleted := deleted || jsonb_build_object('dim_customer', n);
  delete from raw.dim_warehouse      where batch_id = p_batch_id;
  get diagnostics n = row_count; deleted := deleted || jsonb_build_object('dim_warehouse', n);
  delete from raw.plan_monthly_sales where batch_id = p_batch_id;
  get diagnostics n = row_count; deleted := deleted || jsonb_build_object('plan_monthly_sales', n);
  delete from raw.data_quality_hint  where batch_id = p_batch_id;
  get diagnostics n = row_count; deleted := deleted || jsonb_build_object('data_quality_hint', n);

  update raw.load_batches set status = 'rolled_back' where batch_id = p_batch_id;
  return jsonb_build_object('batch_id', p_batch_id, 'deleted', deleted);
end $$;


