-- ---------------------------------------------------------------------
-- FACT SALES — 450 dòng với dữ liệu gốc
-- ⚠️ TUYỆT ĐỐI KHÔNG: select distinct on (order_no, line_no)
--    Làm vậy mất 293.054.650 VNĐ (−5,63%). Xem 005 assert #16.
-- ---------------------------------------------------------------------
create view mart.fact_sales as
select
  sales_sk, batch_id, src_row_index, doc_date, month_start,
  order_no, line_no,
  customer_code, item_code, warehouse_code,
  qty_order, qty_delivered, unit_price, discount_pct,
  doc_status, delivery_due_date, actual_delivery_date, salesperson,
  qty_delivered * unit_price * (1 - discount_pct) as line_revenue,
  -- in_plan_scope TỔNG QUÁT: dòng có khách VÀ item thật (orphan không có
  -- region/category nên nằm ngoài grain plan). Dữ liệu gốc: loại đúng
  -- KH999 + VT999 → 5.134.128.150 / 84.602.000.000 = 6,07% (397 dòng)
  (not flag_orphan_customer and not flag_orphan_item) as in_plan_scope,
  flag_orphan_fk, flag_zero_price, flag_high_discount,
  flag_process_violation, flag_pv_discontinued_item,
  flag_pv_inactive_customer, flag_pv_inactive_warehouse
from stg.fact_sales
where not flag_dup_delete      -- bỏ bản trùng thứ 2 (dữ liệu gốc: index 450)
  and not flag_sentinel;       -- bỏ dòng sentinel   (dữ liệu gốc: index 451)

-- ---------------------------------------------------------------------
-- FACT INVENTORY — điểm gãy #3: LEFT JOIN + giữ dòng orphan kèm cờ.
-- Orphan mới không còn bị nuốt âm thầm; measure loại orphan TƯỜNG MINH
-- bằng flag_orphan_fk. inventory_value của orphan = NULL (không có cost).
-- ---------------------------------------------------------------------
create view mart.fact_inventory as
select
  i.batch_id,
  i.month_end,
  date_trunc('month', i.month_end)::date as month_start,
  i.item_code, i.warehouse_code,
  i.on_hand_qty,                                    -- GIỮ NGUYÊN ÂM. Không set = 0
  i.safety_stock,
  i.last_receipt_date,
  (i.month_end - i.last_receipt_date)               as days_since_receipt,
  i.on_hand_qty * p.standard_cost                   as inventory_value,
  (i.month_end = (select max(month_end) from stg.fact_inventory))
                                                    as is_latest_eom,
  i.flag_negative_stock,
  i.flag_orphan_fk,
  i.flag_below_safety
from stg.fact_inventory i
left join stg.dim_product_current p on p.item_code = i.item_code;

-- ---------------------------------------------------------------------
-- ITEM MOC — điểm gãy #7: sold_6m = cửa sổ 6 THÁNG GẦN NHẤT tính từ
-- snapshot EOM mới nhất, không phải toàn bộ lịch sử.
--
-- ⚠️ Ba filter doc_status khác nhau trong cùng model (ERRATA E2):
--    Revenue Net : <> 'Cancelled'  ·  Fill Rate: IN (Completed, Open)
--    sold_6m/MOC : IN (Completed, Return)   <-- CÁI NÀY
-- ---------------------------------------------------------------------
create view mart.item_moc as
with maxm as (
  select max(month_end) as month_end from mart.fact_inventory
),
eom as (
  select item_code,
         sum(on_hand_qty)     as on_hand_eom,
         sum(inventory_value) as inv_value_eom
  from mart.fact_inventory
  where month_end = (select month_end from maxm)
    and not flag_orphan_fk
  group by item_code
),
sold as (
  select item_code, sum(qty_delivered) as sold_6m
  from mart.fact_sales
  where doc_status in ('Completed','Return')     -- trừ hàng trả lại
    and doc_date > (select month_end from maxm) - interval '6 months'
  group by item_code
),
med as (
  select percentile_cont(0.5) within group (order by inv_value_eom) as median_inv_value
  from eom
)
select
  e.item_code,
  p.item_name, p.category_name, p.abc_class, p.item_status,
  e.on_hand_eom,
  e.inv_value_eom,
  coalesce(s.sold_6m, 0)                                          as sold_6m,
  -- BLANK (null) khi sold_6m = 0, không phải vô cực
  case when coalesce(s.sold_6m,0) > 0
       then e.on_hand_eom / (s.sold_6m / 6.0) end                 as moc,
  m.median_inv_value,
  -- ĐIỀU KIỆN KÉP (§5.2). Xếp hạng chỉ theo MOC cho cảnh báo sai:
  -- VT033 có MOC 65,7 (cao nhất) nhưng chỉ 72,8 tr vốn.
  ( e.inv_value_eom > m.median_inv_value
    and coalesce(s.sold_6m,0) > 0
    and e.on_hand_eom / (s.sold_6m / 6.0) > 12 )                  as is_slow_heavy
from eom e
cross join med m
join stg.dim_product_current p on p.item_code = e.item_code
left join sold s               on s.item_code = e.item_code;

