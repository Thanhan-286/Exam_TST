from stg.fact_inventory i
left join stg.dim_product_current p using (item_code);

-- ---------------------------------------------------------------------
-- Điểm gãy #4: WATERFALL tính ĐỘNG từ dữ liệu (thay bảng tĩnh
-- audit.waterfall_step). Với dữ liệu gốc ra đúng số cũ:
--   A +5.463.412.100 · B −11.956.000 · C −135.000.000 · D −107.785.450
--   E = 5.208.670.650
-- ---------------------------------------------------------------------
create view mart.audit_waterfall as
with s as (
  select *, qty_delivered * unit_price * (1 - discount_pct) as rev
  from stg.fact_sales
)
select 1 as step_order, 'A' as step_code,
       'Gross Revenue (Completed + Open, gồm mọi lỗi)' as step_name,
       (select coalesce(sum(rev),0) from s
        where doc_status in ('Completed','Open'))::numeric as amount,
       false as is_total
union all
select 2, 'B', 'Trừ dòng trùng thật',
       -(select coalesce(sum(rev),0) from s
         where flag_dup_delete and doc_status <> 'Cancelled'), false
union all
select 3, 'C', 'Trừ dòng sentinel / bất khả thi vật lý',
       -(select coalesce(sum(rev),0) from s
         where flag_sentinel and not flag_dup_delete
           and doc_status <> 'Cancelled'), false
union all
select 4, 'D', 'Trừ hàng trả lại (Return)',
       (select coalesce(sum(rev),0) from s
        where doc_status = 'Return'
          and not flag_dup_delete and not flag_sentinel), false
union all
select 5, 'E', 'Revenue Net',
       (select coalesce(sum(line_revenue),0) from mart.fact_sales
        where doc_status <> 'Cancelled'), true;

-- Điểm gãy #4 (tiếp): error_layer với chỉ số tính ĐỘNG, hết ghi cứng 97,79%
create view mart.audit_error_layer as
with dq as (
  select round(100*(1 - count(*) filter (where flag_dup_row or flag_orphan_fk
              or flag_zero_price or flag_high_discount or flag_sentinel)::numeric
              / nullif(count(*),0)), 2) as dq_so
  from stg.fact_sales
),
dqi as (
  select round(100*(1 - count(*) filter (where flag_negative_stock or flag_orphan_fk)::numeric
              / nullif(count(*),0)), 2) as dq_inv
  from stg.fact_inventory
),
pv as (
  select round(100.0*count(*) filter (where flag_process_violation)
              / nullif(count(*),0), 2) as pv
  from stg.fact_sales
)
select 1 as layer_order, 'Dữ liệu' as layer,
       'Có ghi đúng cái đã xảy ra không?' as question,
       'unit_price = 0, dòng trùng, orphan FK' as example,
       'DQ Score SO = ' || (select dq_so from dq) || '% · INV = '
         || (select dq_inv from dqi) || '%' as metric,
       'Người nhập liệu / ETL' as owner
union all
select 2, 'Quy trình', 'Cái đã xảy ra có được phép không?',
       'Bán hàng Discontinued, bán cho khách Inactive',
       'PV Score = ' || (select pv from pv) || '%',
       'Quản lý bán hàng'
union all
select 3, 'Thiết kế', 'Hệ thống có đúng không?',
       'Không có khóa tự nhiên · date sai kiểu · plan khác phạm vi',
       'Không đo bằng % — liệt kê',
       'IT / kiến trúc dữ liệu';

