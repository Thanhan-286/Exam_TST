create view stg.dim_customer_current as
select distinct on (customer_code)
  customer_code, customer_name, region, channel, salesperson, customer_status, batch_id
from stg.raw_dim_customer
order by customer_code, batch_id desc;

create view stg.dim_warehouse_current as
select distinct on (warehouse_code)
  warehouse_code, warehouse_name, region, warehouse_status, batch_id
from stg.raw_dim_warehouse
order by warehouse_code, batch_id desc;

-- Member "(Unknown)" sinh ĐỘNG từ mọi mã orphan thực tế trong fact —
-- hết hardcode KH999/VT999 (điểm gãy #2). Với dữ liệu gốc, kết quả giống
-- hệt cũ: đúng 1 unknown product (VT999) + 1 unknown customer (KH999).
create view stg.dim_product as
select item_code, item_name, category_code, category_name,
       standard_cost, list_price, item_status, abc_class, launch_date,
       false as is_unknown
from stg.dim_product_current
union all
select f.item_code, '(Unknown Item)', '(Unknown)', '(Unknown)',
       null, null, 'Unknown', '(Unknown)', null,   -- standard_cost NULL CỐ Ý (bẫy GM%)
       true
from (select item_code from stg.raw_fact_sales_orders
      union
      select item_code from stg.raw_fact_inventory_eom) f
where f.item_code is not null
  and not exists (select 1 from stg.dim_product_current d
                  where d.item_code = f.item_code);

create view stg.dim_customer as
select customer_code, customer_name, region, channel, salesperson, customer_status,
       false as is_unknown
from stg.dim_customer_current
union all
select distinct f.customer_code, '(Unknown Customer)', '(Unknown)', '(Unknown)',
       '(Unknown)', 'Unknown', true
from stg.raw_fact_sales_orders f
where f.customer_code is not null
  and not exists (select 1 from stg.dim_customer_current d
                  where d.customer_code = f.customer_code);

create view stg.dim_warehouse as
select warehouse_code, warehouse_name,
       region as warehouse_region,   -- ⚠️ ĐỔI TÊN. Chống nhầm với dim_customer.region
       warehouse_status,
       false as is_unknown
from stg.dim_warehouse_current
union all
select f.warehouse_code, '(Unknown Warehouse)', '(Unknown)', 'Unknown', true
from (select warehouse_code from stg.raw_fact_sales_orders
      union
      select warehouse_code from stg.raw_fact_inventory_eom) f
where f.warehouse_code is not null
  and not exists (select 1 from stg.dim_warehouse_current d
                  where d.warehouse_code = f.warehouse_code);

-- ---------------------------------------------------------------------
-- FACT SALES — snapshot active + dedupe toàn-cột, cờ orphan tổng quát
-- ---------------------------------------------------------------------
create view stg.fact_sales as
with dup as (
  -- Rule tái sử dụng được: trùng TOÀN BỘ 14 cột nghiệp vụ (bỏ note, bỏ batch).
  -- Bản ở batch cũ hơn / dòng sớm hơn thắng ⇒ re-upload nhầm file fact cũ
  -- sẽ tự bị loại sạch ở mart (mọi dòng thành bản trùng thứ 2).
  select batch_id, src_row_index,
         count(*) over (partition by
           doc_date, order_no, line_no, customer_code, item_code, warehouse_code,
           qty_order, qty_delivered, unit_price, discount_pct, doc_status,
           delivery_due_date, actual_delivery_date
         ) as n_identical,
         row_number() over (partition by
           doc_date, order_no, line_no, customer_code, item_code, warehouse_code,
           qty_order, qty_delivered, unit_price, discount_pct, doc_status,
           delivery_due_date, actual_delivery_date
           order by batch_id, src_row_index
         ) as rn
  from stg.raw_fact_sales_orders
)
select
  row_number() over (order by f.batch_id, f.src_row_index) as sales_sk,   -- H1
  f.batch_id,
  f.src_row_index,
  f.doc_date,
  date_trunc('month', f.doc_date)::date as month_start,
  f.order_no, f.line_no,
  f.customer_code, f.item_code, f.warehouse_code,
  f.qty_order, f.qty_delivered, f.unit_price, f.discount_pct,
  f.doc_status, f.delivery_due_date, f.actual_delivery_date,
  f.salesperson,                       -- ⚠️ NV BÁN ĐƠN NÀY (§8.1 quyết định #3)
  f.note,

  -- ===== 5 cờ DQ (tầng DỮ LIỆU). Với dữ liệu gốc: tổng = 10 dòng =====
  (d.n_identical > 1)                              as flag_dup_row,
  (d.n_identical > 1 and d.rn > 1)                 as flag_dup_delete,
  (c.customer_code is null)                        as flag_orphan_customer,
  (p.item_code is null)                            as flag_orphan_item,
  (w.warehouse_code is null)                       as flag_orphan_warehouse,
  (c.customer_code is null or p.item_code is null
    or w.warehouse_code is null)                   as flag_orphan_fk,   -- TỔNG QUÁT
  (f.unit_price = 0)                               as flag_zero_price,
  (f.discount_pct > 0.20)                          as flag_high_discount,
  (f.order_no ~ '(9999|0000)$')                    as flag_sentinel,

  -- ===== Cờ Process Violation (tầng QUY TRÌNH) — TÁCH RIÊNG (§6.2) =====
  -- Dòng dữ liệu HOÀN TOÀN ĐÚNG. Cái sai là hành vi bán hàng.
  (p.item_status      = 'Discontinued')            as flag_pv_discontinued_item,
  (c.customer_status  = 'Inactive')                as flag_pv_inactive_customer,
  (w.warehouse_status = 'Inactive')                as flag_pv_inactive_warehouse,
  (coalesce(p.item_status,'')        = 'Discontinued'
    or coalesce(c.customer_status,'')  = 'Inactive'
    or coalesce(w.warehouse_status,'') = 'Inactive') as flag_process_violation
from stg.raw_fact_sales_orders f
join dup d on d.batch_id = f.batch_id and d.src_row_index = f.src_row_index
left join stg.dim_product_current   p on p.item_code      = f.item_code
left join stg.dim_customer_current  c on c.customer_code  = f.customer_code
left join stg.dim_warehouse_current w on w.warehouse_code = f.warehouse_code;

-- ---------------------------------------------------------------------
-- FACT INVENTORY — trong snapshot active, chống trùng theo khóa tự nhiên
-- ---------------------------------------------------------------------
create view stg.fact_inventory as
with latest as (
  select distinct on (month_end, item_code, warehouse_code) *
  from stg.raw_fact_inventory_eom
  order by month_end, item_code, warehouse_code, batch_id desc
)
select
  i.batch_id,
  i.month_end,
  i.item_code, i.warehouse_code,
  i.on_hand_qty,
  i.inventory_value,       -- giữ để reconcile §6.6, KHÔNG đưa lên mart
  i.safety_stock,
  -- H6: Excel serial epoch = 1899-12-30 (KHÔNG phải 1900-01-01)
  (date '1899-12-30' + (i.last_receipt_serial || ' days')::interval)::date
                                             as last_receipt_date,
  i.last_receipt_serial,
  i.stock_status_note,
  (i.on_hand_qty < 0)                        as flag_negative_stock,
  (p.item_code is null)                      as flag_orphan_item,
  (w.warehouse_code is null)                 as flag_orphan_warehouse,
  (p.item_code is null or w.warehouse_code is null) as flag_orphan_fk,  -- TỔNG QUÁT
  (i.on_hand_qty < i.safety_stock)           as flag_below_safety
from latest i
left join stg.dim_product_current   p on p.item_code      = i.item_code
left join stg.dim_warehouse_current w on w.warehouse_code = i.warehouse_code;

